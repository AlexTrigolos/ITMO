class FirstClass {
    //public int num;
    //public String name;
    public def num;
    public def name;

    public FirstClass(int num, String name) {
        this.num = num;
        this.name = name;
    }

    int getNumber() {
        return num
    }

    void setNumber(int num) {
        this.num = num
    }

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }

    int NumberX2() {
        num * num;
    }

    Integer numInt(Integer n) {
        n
    }

    int numint(Integer n) {
        n
    }
}
